    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String telefone = txtTelefone.getText();
        try{
            int apartamento = Integer.parseInt(txtApartamento.getText());
            int torre = Integer.parseInt(txtTorre.getText());
            MoradorController.definirMorador(nome, cpf, apartamento, torre, telefone);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Numero da torre inv√°lido!");
        }         
    }//GEN-LAST:event_btnSalvarActionPerformed

